class Berita {
  String Judul;
  String Tanggal;
  String Tahun;
  String Deskipsi;
  String image_url;

   Berita.isi_berita(String judul, String tanggal, String tahun, String deskripsi, String image_url){
      this.Judul = judul;
      this.Tanggal = tanggal;
      this.Tahun = tahun;
      this.Deskipsi = deskripsi;
      this.image_url = image_url;
  }
}